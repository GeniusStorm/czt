package com.ztel.app.vo.gis;

import java.math.BigDecimal;
import java.util.Date;

public class DeliveryInfoVo {
	
    private String routecode;
    private BigDecimal totalcount;
    private BigDecimal signcount;
    private BigDecimal durationlen;
    private BigDecimal perdurationlen;
    private Date orderdate;
    private String searchDate;
    
	public BigDecimal getDurationlen() {
		return durationlen;
	}
	public void setDurationlen(BigDecimal durationlen) {
		this.durationlen = durationlen;
	}
	public BigDecimal getPerdurationlen() {
		return perdurationlen;
	}
	public void setPerdurationlen(BigDecimal perdurationlen) {
		this.perdurationlen = perdurationlen;
	}
	public Date getOrderdate() {
		return orderdate;
	}
	public void setOrderdate(Date orderdate) {
		this.orderdate = orderdate;
	}
	public String getSearchDate() {
		return searchDate;
	}
	public void setSearchDate(String searchDate) {
		this.searchDate = searchDate;
	}
	public String getRoutecode() {
		return routecode;
	}
	public void setRoutecode(String routecode) {
		this.routecode = routecode;
	}
	public BigDecimal getTotalcount() {
		return totalcount;
	}
	public void setTotalcount(BigDecimal totalcount) {
		this.totalcount = totalcount;
	}
	public BigDecimal getSigncount() {
		return signcount;
	}
	public void setSigncount(BigDecimal signcount) {
		this.signcount = signcount;
	}
}
